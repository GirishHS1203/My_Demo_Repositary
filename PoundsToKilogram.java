public class PoundsToKilogram {
    public static void main(String[] args) {
        double pounds=0.45359237d;
        double kilogram=15;
        double total= kilogram* pounds;
        System.out.println(total);

        double mypounds=15;
        double myKilograms= mypounds * 0.45359239d;
        System.out.println(myKilograms);
    }
}
